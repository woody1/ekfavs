<h3>Dorset Apple Cake Recipe</h3>

<p>Here is an opportunity for the men (and boys) of East Knoyle to show what they are made of… or rather what a Dorset Apple Cake is made of. Hint: follow the recipe below exactly, as any deviation from the instructions may result in disqualification.</p>

<h4>Ingredients</h4>

<ul>
    <li>115g unsalted butter, diced and chilled, plus extra for the tin	</li>
    <li>225g self-raising flour</li>
    <li>2 tsp ground cinnamon</li>
    <li>115g light brown sugar</li>
    <li>1 large egg, beaten</li>
    <li>6-8 tbsp milk</li>
    <li>100g sultanas</li>
    <li>2 tbsp demerara sugar</li>
    <li>225g Bramley or Granny Smith apples, peeled, cored and diced</li>
</ul>

<p>Heat the oven to 180C/160C fan/gas 4. Butter and line a deep 20cm cake tin with baking parchment. (Hint: If you have buttered the sides carefully you only need to cut a circle to fit the bottom of the cake tin.)</p>

<p>Mix the flour and cinnamon together in a large bowl. Add the butter and rub into the flour using your fingers, until it resembles fine breadcrumbs. Stir in the light brown sugar. Beat in the egg followed by 6 to 8 tbsp of milk – add it gradually until you have a smooth, thick batter.</p>

<p>Add the apples and sultanas and mix to combine. Scrape the batter into the prepared tin and gently level out using the back of a spoon. Sprinkle over the demerara sugar, and bake for 30-40 mins in the centre of the oven until golden and a skewer inserted into the middle comes out clean.</p>

<p>Allow to cool in the tin for 15 mins and then carefully insert a knife round the edges of the tin and turn cake out onto a wire rack to cool further, before displaying on a plate.</p>