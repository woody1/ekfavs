<h3>Frosted Courgette and Lemon Cake</h3>
<h4>Ingredients</h4>
<ul>
    <li>3 lemons</li>
    <li>200g caster sugar</li>
    <li>3 eggs</li>
    <li>200g soft butter plus extra for the tins</li>
    <li>2 medium courgettes, coarsely grated (you will need 300g flesh)</li>
    <li>1 tsp vanilla extract</li>
    <li>100g self-raising flour</li>
    <li>100g wholemeal flour</li>
    <li>1 tsp baking powder</li>
    <li>¼ tsp salt</li>
    <li>1tsp poppy seed plus extra to decorate</li>
</ul>

<h4>For the drizzle</h4>
<ul>
    <li>25g icing sugar</li>
    <li>1tbs lemon juice</li>
</ul>

<h4>For the frosting</h4>
<ul>
    <li>50g icing sugar</li>
    <li>50g butter</li>
    <li>200g pack full-fat soft cheese</li>
    <li>2 tbs lemon juice</li>
    <li>Zest of a lemon</li>
    <li>4 tbsp lemon curd</li>
</ul>

<h4>Method</h4>
<p class="bold">Step 1</p>
<p>Heat the oven to 180c/160c fan/gas 4.  Butter two x 20cm sandwich tins and line the bases with baking parchment. Zest 2 lemons, then squeeze the juice into a separate bowl. Put 200g butter, the caster sugar, eggs, courgettes, poppy seeds, vanilla and lemon zest into a mixing bowl. Beat to a creamy batter. Stir in one tablespoon of lemon juice, the flours, baking powder and ¼ tsp salt. Spoon the mix into the tins, then bake for 25 minutes or until risen, golden and springy in the middle.</p>

<p class="bold">Step 2</p>
<p>Make a drizzle by mixing another tablespoon of lemon juice with 25g of icing sugar. To make the frosting, put 50g icing sugar and butter into a bowl, add the soft cheese, remaining lemon juice (about 2 tablespoons) and grate in the final lemon zest. Beat until creamy and smooth.</p>

<p class="bold">Step 3</p>
<p>When the cakes are ready, cool for 15 minutes in their tins, then turn onto a cooling rack. Prick several times with a cocktail stick, spoon over the drizzle and cool completely.</p>
<p>Make sure that they are really cold before adding the frosting. Put one cake onto a serving plate and spread with just under half the frosting. Spread over the lemon curd. Top with the second cake, spread the remaining frosting over the top and sprinkle with poppy seeds.</p>



